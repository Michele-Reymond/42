int	ft_atoi(const char *str)
{
	int	i;
	int	num;
	int	temp;

	i = 0;
	temp = 0;
	num = 1;
	if (str[i] == '-')
		num = num * -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		temp = temp * 10 + (str[i] - 48);
		i++;
	}
	temp = temp * num;
	return (temp);
}
